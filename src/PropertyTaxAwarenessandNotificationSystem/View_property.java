private void getzone(){
    try {
        Connection con=ConnectionProvider.getCon();
            PreparedStatement st = con.prepareStatement("SELECT DISTINCT zone FROM property");
        ResultSet rs = st.executeQuery();
        while(rs.next()){
            String mycat = rs.getString("zone");
            zone.addItem(mycat);
        }
    } catch (Exception e) {
        System.out.println(e.getMessage());
    }
}      
private void getcity(){
    try {
        Connection con=ConnectionProvider.getCon();
            PreparedStatement st = con.prepareStatement("SELECT DISTINCT city FROM property");
        ResultSet rs = st.executeQuery();
        while(rs.next()){
            String mycat = rs.getString("city");
            city.addItem(mycat);
        }
    } catch (Exception e) {
        System.out.println(e.getMessage());
    }
}      






public void selectres(){
     
    try {
        String zoneee=zone.getSelectedItem().toString();
        String cityyy=city.getSelectedItem().toString();
       Connection con=ConnectionProvider.getCon();
       PreparedStatement ps = con.prepareStatement("SELECT * FROM property WHERE zone = '"+zoneee+"' AND city='"+cityyy+"'");
      ResultSet  rs = ps.executeQuery();
        ResultSetMetaData rsmd = rs.getMetaData();

        DefaultTableModel tbl = new DefaultTableModel();

        int col = rsmd.getColumnCount();
        String[] colname = new String[col];
        for (int i = 0; i < col; i++) {
            colname[i] = rsmd.getColumnName(i + 1).toUpperCase();
        }

        tbl.setColumnIdentifiers(colname);

        while (rs.next()) {
            String namee =rs.getString(1);
            String catee = rs.getString(2);
             String floorr = String.valueOf(rs.getInt(3));
             String sqftt = String.valueOf(rs.getDouble(4));
            String ownerr = rs.getString(5);
            String wardd = String.valueOf(rs.getInt(6));
            String zonee = rs.getString(7);
            String cityy=rs.getString(8);
            

            String[] tdata = {namee,catee,floorr,sqftt,ownerr,wardd,zonee,cityy};
            tbl.addRow(tdata);
        }

        // Set the new model to the JTable
        table.setModel(tbl);
        con.close();
    } catch (Exception e) {
        System.out.println(e.getMessage());
    }


    }
