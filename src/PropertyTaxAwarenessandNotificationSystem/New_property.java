    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (owner.getText().isEmpty() || ward.getText().isEmpty() || name.getText().isEmpty() || cate.getSelectedIndex() == 0 || floor.getText().isEmpty() || zone.getText().isEmpty()|| sq.getText().isEmpty()|| city.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "PLEASE FILL DEATILS");        }
else{
            String namee=name.getText();
            String catee=cate.getSelectedItem().toString();
            String floorr=floor.getText();
            String sqq=sq.getText();
            String ownerr=owner.getText();
            String wardd=ward.getText();
            String zonee=zone.getText();
            String cityy=city.getText();
            try
            {
                Connection con=ConnectionProvider.getCon();
                PreparedStatement ps=con.prepareStatement("insert into property values(?,?,?,?,?,?,?,?)");
                ps.setString(1, namee);
                ps.setString(2,catee);
                ps.setString(3,floorr);
                ps.setString(4,sqq);
                ps.setString(5,ownerr);
                ps.setString(6,wardd);
                ps.setString(7,zonee);
                ps.setString(8,cityy);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Property Details Added Successfully");
                clear();
                new Login_user().setVisible(true);
                this.dispose();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }}
    }//GEN-LAST:event_jButton1ActionPerformed

